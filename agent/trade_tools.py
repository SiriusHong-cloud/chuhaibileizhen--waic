def search_hs_code(keyword: str = "", category: str = "") -> list:
    """搜索HS编码"""
    results = []
    keyword = keyword.lower() if keyword else ""
    for item in HS_CODE_DATABASE:
        match = True
        if keyword:
            if keyword not in item["code"].lower() and keyword not in item["name"].lower() and keyword not in item["description"].lower():
                match = False
        if category and category != "全部":
            if category not in item["category"]:
                match = False
        if match:
            results.append(item)
    return results


